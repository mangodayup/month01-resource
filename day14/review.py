"""
    面向对象
        字面意思:考虑问题从对象的角度出发
        思考流程:
            现实事物 -抽象化-> 类 -具体化-> 对象
        三大特征:
            封装[分]:分而治之,(行为)变则疏之
                例如:创建人类/汽车/飞机
            继承[隔]:统一变化,隔离变化
                例如:创建交通工具,在运输行为上约束汽车/飞机
                    人类获得了一种使用方式
            多态[做]:继承将相关类型的共性进行统一
                例如:火车汽车都有运输方法
                多态在继承基础上体现类型的个性
                例如:火车汽车通过重写实现自己的运输方式
        设计原则:
            开闭原则:可以增加新功能,但不修改客户端代码
                例如:增加轮船,人类不需要发生变化
            单一职责:一个类有且只有一个改变的原因
            依赖倒置:客户端代码使用父类不使用子类
                    子类看着父类写代码
                例如:人类使用交通工具而不是使用火车汽车
            组合复用:如果仅仅是代码的复用,
                   优先使用组合关系,
                   而不是继承关系.
                   继承语义-是一种
                   组合语义-有一个
                例如:人类通过变量调用运输函数,而不是通过继承.
            迪米特:低耦合
                例如:人类与火车汽车被交通工具隔离
            里氏替换:
                形参是父类型,实参可以任意子类型
                子类重写时建议先super()调用父方法

"""
