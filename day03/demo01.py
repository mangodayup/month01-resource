"""
    数据类型 - 字面值(各种写法)
        整形int
        浮点型float
"""
# 1. 整形int
# -- 十进制DEC：每位用十种状态计数，逢十进一，写法是0~9。
num01 = 10
# -- 二进制BIN：每位用二种状态计数，逢二进一，写法是0~1。
# 0 1 10 11  100  101
num02 = 0b10
# -- 八进制OCT：每位用八种状态计数，逢八进一，写法是0~7。
# 0~7  10  11
num03 = 0o10
# -- 十六进制HEX：每位用十六种状态计数，逢十六进一，写法是0~9  a(10)~f(15)。
# 0~9  a b c d e f 10 11
num04 = 0x10
print(num04)

# 2. 浮点型float
# -- 小数
num05 = 1.23
# -- 科学计数法
num06 = 1e-5
print(0.00001)


