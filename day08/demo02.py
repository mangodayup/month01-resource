"""
    函数内存分布
"""


# 1. 将函数代码加载到内存中,函数体不执行.
def func01():
    a = 10
    print("func01执行了")


# 2. 调用函数时,在内存中开辟空间(栈帧),
#    存储在函数内部创建的变量。
func01()


# 3. 函数执行后,栈帧立即释放.


def func02(p1, p2):
    # 修改栈帧中的变量
    p1 = 100
    # 修改传入的列表
    p2[0] = 200


data01 = 10
data02 = [20]
func02(data01, data02)
print(data01)  # 10
print(data02)  # 200
